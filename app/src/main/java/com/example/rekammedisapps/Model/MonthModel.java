package com.example.rekammedisapps.Model;

public class MonthModel {
    private int bulan;
    private int tahun;

    public MonthModel(){

    }

    public int getBulan() {
        return bulan;
    }

    public void setBulan(int bulan) {
        this.bulan = bulan;
    }

    public int getTahun() {
        return tahun;
    }

    public void setTahun(int tahun) {
        this.tahun = tahun;
    }
}
